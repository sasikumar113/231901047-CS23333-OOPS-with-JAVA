class operator {
  public static void main(String[] args) {
    

    int a = 12, b = 5;

    System.out.println("a + b = " + (a + b));

    System.out.println("a - b = " + (a - b));


    System.out.println("a * b = " + (a * b));

    // division operator
    System.out.println("a / b = " + (a / b));

    // modulo operator
    System.out.println("a % b = " + (a % b));
  }
}